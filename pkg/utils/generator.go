package utils

import (
	"fmt"
	"math/rand"
	"time"
)

func GenID(max int) int32 {
	rand.Seed(time.Now().UnixNano())
	return int32(rand.Intn(max))
}

func GenIP() string {
	ip := fmt.Sprintf("%d:%d:%d:%d", GenID(255), GenID(255), GenID(255), GenID(255))
	return ip
}

